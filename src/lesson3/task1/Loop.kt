 * Использовать операции со строками в этой задаче запрещается.
 */
fun revert(n: Int): Int {
    val count = digitNumber(n)
    var sum = 0
    var number = n
    var last: Int
    var digit = 0
    for (i in 1..count) {
        digit += 1
        last = number % 10
        sum += last * (10.0.pow(count - digit).toInt())
        number /= 10
    }
    return sum
}

